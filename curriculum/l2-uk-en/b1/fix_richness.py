import re

filepath = "/Users/krisztiankoos/projects/learn-ukrainian/curriculum/l2-uk-en/b1/relative-clauses-koly-shcho.md"
with open(filepath, encoding="utf-8") as f:
    content = f.read()

# Fix euphony
content = content.replace("в граматичній площині", "у граматичній площині")

# Fix dialogues (adding blockquotes)
content = re.sub(r'^\*\*(Оксана|Андрій|Олена|Тарас):\*\* ', r'> **\1:** ', content, flags=re.MULTILINE)

# Add cultural anchors
anchor1 = """
> 🇺🇦 **Культурний момент**
>
> Вечірні прогулянки історичним центром Львова — це справжня традиція для місцевих жителів та туристів. Саме в такі моменти можна відчути атмосферу старого міста, коли кавові пахощі змішуються зі звуками вуличної музики.
> У граматичному сенсі це ідеальна ілюстрація паралельних процесів.
> *Коли ми пили львівську каву, музиканти грали старовинні мелодії.*
"""

content = content.replace(
    "«Він намагався слухати лекцію та одночасно конспектувати найважливіші тези». (Використання прислівника для підкреслення здатності виконувати дії в один і той самий час).",
    "«Він намагався слухати лекцію та одночасно конспектувати найважливіші тези». (Використання прислівника для підкреслення здатності виконувати дії в один і той самий час).\n" + anchor1
)

anchor2 = """
> 🇺🇦 **Культурний момент**
>
> Відвідування Київського оперного театру вимагає дотримання певного етикету та послідовності дій. Традиційно кияни планують свій вечір так, щоб залишити час на обговорення вистави.
> Використання конструкцій послідовності допомагає точно передати цю хронологію.
> *Після того як закінчилася вистава, ми пішли обговорювати її до найближчої кав'ярні на Хрещатику.*
"""

content = content.replace(
    "«Коли концерт закінчився, глядачі ще довго аплодували артистам стоячи». (Класична послідовність: закінчення викликало оплески).",
    "«Коли концерт закінчився, глядачі ще довго аплодували артистам стоячи». (Класична послідовність: закінчення викликало оплески).\n" + anchor2
)

anchor3 = """
> 🇺🇦 **Культурний момент**
>
> У Карпатах існує безліч унікальних традицій, ремесел та предметів побуту, які часто стають об'єктом захоплення дослідників. Описуючи ці артефакти, важливо використовувати відносні займенники для створення плавного тексту.
> Це дозволяє передати автентичність та унікальність карпатської культури без зайвих повторень.
> *Це той самий гуцульський ліжник, що майстриня ткала протягом кількох місяців.*
"""

content = content.replace(
    "«Люди, що зібралися на головній площі, вимагали негайних рішень від керівництва міста». (Тут «що» = «люди», виконує роль активного суб'єкта).",
    "«Люди, що зібралися на головній площі, вимагали негайних рішень від керівництва міста». (Тут «що» = «люди», виконує роль активного суб'єкта).\n" + anchor3
)

with open(filepath, "w", encoding="utf-8") as f:
    f.write(content)

print("Modifications applied.")
