# 1. Complex sentences
# Let's match up to the end of the sentence and replace it with a shorter split version.
# Since we don't know the exact text, we can use regex that captures the sentence and splits it by a conjunction or comma.

def split_long(pattern, text, replace_with):
    # This will search for the regex pattern and replace it
    new_text = re.sub(pattern, replace_with, text, flags=re.DOTALL)
    if new_text == text:
        # Just to help us debug which ones missed
        print(f"Warning: pattern failed: {pattern[:30]}...")
    return new_text

