        "",
        "</PrimaryReading>",
        "",
        f"**Джерело:** {metadata['source']}",
        "",
        f"**Де вивчають:** {study_links}",
        "",
        f"{{/* {GENERATED_MARKER}; source_chunk_id: {corpus.chunk_id} */}}",
