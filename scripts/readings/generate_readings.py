"""Generate Хрестоматія reading MDX files from folk primary-reading blocks.

The generator is deliberately conservative: it only writes a reading when a
folk module contains a structured ``:::primary-reading`` block, the quoted
fragment can be matched to a public-domain literary corpus row, and the output
slug is not already occupied by a hand-authored reading.
"""

