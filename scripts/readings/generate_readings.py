_PRIMARY_BLOCK_RE = re.compile(r":::primary-reading\s*\n(?P<body>.*?)\n:::\s*", re.DOTALL)
_ATTRIBUTION_RE = re.compile(
    r"^—\s*(?P<author>[^,\n]+),\s*«(?P<title>[^»]+)»(?:\s*\((?P<note>[^)\n]+)\))?",
    re.MULTILINE,
)
_STRESS_RE = re.compile("[\u0300\u0301]")
_NON_WORD_RE = re.compile(r"[^\w]+", re.UNICODE)


@dataclass(frozen=True)
class ResourceHint:
    title: str
    source_ref: str
    role: str
    notes: str
    packet_chunk_id: str | None


@dataclass(frozen=True)
class PrimaryReadingCandidate:
    module_dir: Path
    track: str
    slug: str
    author: str
    title: str
    note: str
    quote_lines: tuple[str, ...]

    @property
    def work_key(self) -> str:
        return f"{_text_key(self.author)}::{_text_key(self.title)}"


@dataclass(frozen=True)
class CorpusText:
    chunk_id: str
    author: str
    work: str
    work_id: str
    year: int | None
    genre: str
    language_period: str
    source_file: str
    source_url: str
    text: str


@dataclass(frozen=True)
class VerificationResult:
    matched: bool
    confidence: float
    detail: str


@dataclass
class ResolvedReading:
    candidate: PrimaryReadingCandidate
    corpus: CorpusText
    slug: str
    module_dirs: list[Path] = field(default_factory=list)
    tracks: set[str] = field(default_factory=set)


@dataclass(frozen=True)
class WrittenReading:
    slug: str
    path: Path
    action: str
    source_chunk_id: str


@dataclass(frozen=True)
class SkippedReading:
    title: str
    slug: str | None
    reason: str


@dataclass(frozen=True)
class GenerationSummary:
    written: tuple[WrittenReading, ...]
    skipped: tuple[SkippedReading, ...]
    existing: tuple[WrittenReading, ...]

    @property
    def generated_count(self) -> int:
        return sum(1 for item in self.written if item.action == "created")


QuoteVerifier = Callable[[PrimaryReadingCandidate, CorpusText], VerificationResult]


