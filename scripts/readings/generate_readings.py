def format_summary(summary: GenerationSummary, *, project_root: Path = PROJECT_ROOT) -> str:
    lines = [
        "Readings generation summary",
        f"Written: {len(summary.written)}",
        f"Existing/skipped because present: {len(summary.existing)}",
        f"Skipped: {len(summary.skipped)}",
    ]
    for item in summary.written:
        lines.append(
            f"- {item.action}: {_display_path(item.path, project_root)} "
            f"(chunk {item.source_chunk_id})"
        )
    for item in summary.existing:
        lines.append(
            f"- {item.action}: {_display_path(item.path, project_root)} "
            f"(chunk {item.source_chunk_id})"
        )
    for item in summary.skipped:
        suffix = f" [{item.slug}]" if item.slug else ""
        lines.append(f"- skipped{suffix}: {item.title} — {item.reason}")
    return "\n".join(lines)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
