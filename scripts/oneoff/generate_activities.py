for s, adj in sentences_act1:
    pair = next((p for p in adj_adv_pairs if p[0] == adj), None)
    if pair:
        adv, dist1, dist2 = pair[1], pair[0], pair[2]
        dist3 = pair[3]
        if adv == "добре":
            options = ["добре", "добрий", "добра", "добрі"]
        else:
            options = [adv, dist1, dist2, dist3]
        act1_items.append({
            "sentence": s,
            "answer": adv,
            "options": options
        })

