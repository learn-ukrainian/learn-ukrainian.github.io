"""Export Ukrainian Pedagogical Distillation & Alignment Dataset for UNLP Researchers.

Transforms Hramatka authoring benchmark artifacts (raw outputs, Tour 3/4 repairs,
and Gemini 3.6 Flash gold lessons) into a structured fine-tuning dataset for
open-weights LLM alignment (Gemma, Llama, Mistral).
"""

import json
import sqlite3
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[2]
PRIVATE_ROOT = Path("/Users/krisztiankoos/projects/learn-ukrainian-infra-private")
DATASET_DIR = REPO_ROOT / "data" / "datasets" / "hramatka_uk_pedagogy_v1"
DATASET_DIR.mkdir(parents=True, exist_ok=True)


def export_pedagogy_dataset() -> dict:
    # 1. Load Anchor Text
    anchor_path = PRIVATE_ROOT / ".agent" / "tmp" / "rent_anchor.txt"
    anchor_text = anchor_path.read_text(encoding="utf-8") if anchor_path.exists() else ""

    records = []

    # 2. Extract Gemini 3.6 Flash Gold Lesson
    gemini_gold_path = PRIVATE_ROOT / ".agent" / "tmp" / "gemini-3.6-flash-bakeoff" / "gemini-3.6-flash" / "lesson.json"
    if gemini_gold_path.exists():
        gemini_data = json.loads(gemini_gold_path.read_text(encoding="utf-8"))
        records.append(
            {
                "id": "hramatka-lviv-rent-gemini-3.6-gold",
                "anchor_text": anchor_text,
                "level": "B1",
                "model_seat": "gemini-3.6-flash",
                "quality_tier": "gold_pass",
                "activity_count": len(gemini_data.get("activities", [])),
                "density_score": 10.0,
                "lesson": gemini_data,
                "annotation": "Gold reference lesson. High activity density (8 activities), deep B1 comparative morphology instruction, 100% VESUM compliance.",
            }
        )

    # 3. Extract Gemma 4 31B Raw vs Elevated Pair
    gemma_raw_path = PRIVATE_ROOT / ".agent" / "tmp" / "gemini-3.6-flash-bakeoff" / "gemma-31b-or" / "lesson.json"
    gemma_elevated_path = (
        PRIVATE_ROOT / ".agent" / "tmp" / "gemini-3.6-flash-bakeoff" / "elevated" / "gemma-31b-or" / "lesson.json"
    )

    if gemma_raw_path.exists() and gemma_elevated_path.exists():
        gemma_raw = json.loads(gemma_raw_path.read_text(encoding="utf-8"))
        gemma_elevated = json.loads(gemma_elevated_path.read_text(encoding="utf-8"))

        records.append(
            {
                "id": "hramatka-lviv-rent-gemma-31b-alignment-pair",
                "anchor_text": anchor_text,
                "level": "B1",
                "model_seat": "google-ais/gemma-4-31b-it",
                "quality_tier": "alignment_pair",
                "raw_input_lesson": gemma_raw,
                "raw_activity_count": len(gemma_raw.get("activities", [])),
                "elevated_target_lesson": gemma_elevated,
                "elevated_activity_count": len(gemma_elevated.get("activities", [])),
                "delta_fixes": [
                    "Normalized JSON schema field names (instructions -> instruction, questions -> items)",
                    "Stripped English (True)/(False) labels in favor of Ukrainian (правда / неправда)",
                    "Added missing superlative target word 'найважливіше' to mark-the-words",
                    "Replaced factual recall error corrections with real B1 Ukrainian morphosyntax repairs",
                ],
                "annotation": "Direct DPO/SFT preference pair for open-weights Gemma alignment to elevate activity density, schema compliance, and Ukrainian immersion.",
            }
        )

    # 4. Save JSONL dataset
    jsonl_path = DATASET_DIR / "hramatka_uk_pedagogy_v1.jsonl"
    with open(jsonl_path, "w", encoding="utf-8") as f:
        for r in records:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")

    # 5. Save README.md for UNLP researchers
    readme_content = """# Hramatka Ukrainian Pedagogical Alignment Dataset (`hramatka-uk-pedagogy-v1`)

> **Target Audience**: UNLP (Ukrainian Natural Language Processing) researchers, open-model fine-tuners, and AI alignment engineers.
> **Purpose**: Fine-tuning open-weights models (Gemma 4, Llama 4, Mistral) to achieve Gemini 3.6 Flash level activity density, pedagogical rigor, and 100% Ukrainian immersion.

---

## Dataset Overview

The dataset contains structured B1 Ukrainian lesson authoring pairs generated from authentic Ukrainian anchor texts (*«Як ми шукали квартиру у Львові»*).

- **Gold Reference Lessons**: Exemplary lessons generated by Gemini 3.6 Flash featuring 8 distinct activity types (`quiz`, `true-false`, `cloze`, `match-up`, `mark-the-words`, `fill-in`, `error-correction`, `text-questions`), high item density (>= 5 items per activity), and 100% VESUM morphosyntactic verification.
- **SFT / DPO Preference Pairs**: Direct alignment pairs contrasting raw Gemma 4 31B outputs (which suffer from field casing issues, shallow item density, and English label leaks) against elevated gold targets.

---

## Dataset Schema (`hramatka_uk_pedagogy_v1.jsonl`)

Each line in `hramatka_uk_pedagogy_v1.jsonl` contains:
```json
{
  "id": "hramatka-lviv-rent-gemma-31b-alignment-pair",
  "anchor_text": "... authentic Ukrainian B1 anchor text ...",
  "level": "B1",
  "model_seat": "google-ais/gemma-4-31b-it",
  "quality_tier": "alignment_pair",
  "raw_input_lesson": { "activities": [...] },
  "elevated_target_lesson": { "activities": [...] },
  "delta_fixes": [
    "Normalized JSON schema field names (instructions -> instruction, questions -> items)",
    "Stripped English (True)/(False) labels in favor of Ukrainian (правда / неправда)",
    "Added missing superlative target word 'найважливіше' to mark-the-words",
    "Replaced factual recall error corrections with real B1 Ukrainian morphosyntax repairs"
  ]
}
```

---

## How to Use for Fine-Tuning Gemma 4 31B

1. **Supervised Fine-Tuning (SFT)**: Train Gemma 4 31B using `anchor_text` as prompt and `elevated_target_lesson` as target completion.
2. **Direct Preference Optimization (DPO)**: Use `raw_input_lesson` as rejected response and `elevated_target_lesson` / `gemini_gold` as chosen response.

---

*Dataset exported automatically from the Learn Ukrainian Project benchmark suite.*
"""
    (DATASET_DIR / "README.md").write_text(readme_content, encoding="utf-8")

    print(f"Exported {len(records)} records to {jsonl_path}")
    return {"status": "ok", "records_exported": len(records), "dataset_path": str(jsonl_path)}


if __name__ == "__main__":
    export_pedagogy_dataset()
