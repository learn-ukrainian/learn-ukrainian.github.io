for old, new in replaces:
    # Use simple replacing, ignore small whitespace if not matched
    text = text.replace(old, new)

# To ensure things that had slightly different whitespaces also match, let's use regex
for old, new in replaces:
    escaped_old = re.escape(old).replace(r'\ ', r'\s+').replace(r'\n', r'\s+')
    text = re.sub(escaped_old, new, text)

# Now iterate and find any remaining sentences > 30 words
# We can do a basic split on `. `, `! `, `? `
sentences = re.split(r'(?<=[.!?])\s+', text)
new_sentences = []
for s in sentences:
    words = [w for w in s.split() if re.search(r'[А-Яа-яЄєІіЇїҐґ]', w)]
    if len(words) > 30 and not s.startswith(">") and not s.startswith("|") and not s.startswith("#") and "!" not in s:
        # Split on commas with specific conjunctions
        s = re.sub(r', а ', '. А ', s, 1)
        s = re.sub(r', що ', '. Це ', s, 1)
        s = re.sub(r' та ', '. ', s, 1)
    new_sentences.append(s)

text = " ".join(new_sentences)

