import re

def split_long_sentence(sentence):
    words = re.findall(r'\b\w+\b', sentence)
    if len(words) <= 30:
        return sentence
    
    # Try to find a good splitting point: conjunctions like 'тому що', 'адже', 'або', 'що', 'який', 'де'
    # Or just replace the first comma that is roughly in the middle with a period.
    comma_splits = sentence.split(', ')
    if len(comma_splits) > 1:
        mid = len(comma_splits) // 2
        p1 = ", ".join(comma_splits[:mid])
        p2 = ", ".join(comma_splits[mid:])
        # Capitalize p2
        if p2:
            p2 = p2[0].upper() + p2[1:]
        return p1 + ". " + p2
    return sentence

