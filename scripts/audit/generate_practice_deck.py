def _json_bytes(payload: dict[str, Any]) -> bytes:
    text = json.dumps(payload, ensure_ascii=False, indent=2, sort_keys=True) + "\n"
    return text.encode("utf-8")


def _size_budget(payload: dict[str, Any], raw_limit: int, gzip_limit: int) -> dict[str, int | bool]:
    raw = _json_bytes(payload)
    gzipped = gzip.compress(raw, mtime=0)
    return {
        "rawBytes": len(raw),
        "gzipBytes": len(gzipped),
        "rawLimitBytes": raw_limit,
        "gzipLimitBytes": gzip_limit,
        "ok": len(raw) <= raw_limit and len(gzipped) <= gzip_limit,
    }


def apply_size_budgets(
    shards: dict[str, dict[str, dict[str, Any]]],
    raw_limit: int,
    gzip_limit: int,
) -> None:
    for level, level_shards in shards.items():
        while True:
            oversized: list[dict[str, Any]] = []
            for payload in level_shards.values():
                payload["sizeBudget"] = _size_budget(payload, raw_limit, gzip_limit)
                if not payload["sizeBudget"]["ok"]:
                    oversized.append(payload)
            if not oversized:
                break

            schemas = ", ".join(f"{payload['schema']}.{level}" for payload in oversized)
            print(f"WARN: {schemas} exceeds size budget; trimming {level} shard", file=sys.stderr)
            index_items = level_shards.get("index", {}).get("items", [])
            if not isinstance(index_items, list) or not index_items:
                break
            removed = index_items.pop()
            removed_lemma_id = removed.get("lemmaId") if isinstance(removed, dict) else None
            if not removed_lemma_id:
                break
            lexemes = level_shards.get("lexemes", {}).get("lexemes", [])
            if isinstance(lexemes, list):
                lexemes[:] = [
                    lexeme
                    for lexeme in lexemes
                    if not isinstance(lexeme, dict) or lexeme.get("lemmaId") != removed_lemma_id
                ]
            cloze_items = level_shards.get("cloze", {}).get("cloze", [])
            if isinstance(cloze_items, list):
                cloze_items[:] = [
                    item
                    for item in cloze_items
                    if not isinstance(item, dict) or item.get("lemmaId") != removed_lemma_id
                ]
            for mode in DRILL_MODES:
                mode_items = level_shards.get(mode, {}).get(MODE_BODY_KEYS[mode], [])
                if isinstance(mode_items, list):
                    mode_items[:] = [
                        item
                        for item in mode_items
                        if not isinstance(item, dict) or item.get("lemmaId") != removed_lemma_id
                    ]
            counts = level_shards.get("index", {}).get("counts")
            if isinstance(counts, dict) and isinstance(lexemes, list) and isinstance(cloze_items, list):
                counts["lexemes"] = len(lexemes)
                counts["cloze"] = len(cloze_items)
                counts["clozeEligibleLexemes"] = len(
                    {item["lemmaId"] for item in cloze_items if isinstance(item, dict)}
                )
                counts["clozeCoverage"] = (
                    round(counts["clozeEligibleLexemes"] / len(lexemes), 4) if lexemes else 0
                )
                mode_counts = counts.setdefault("modeCounts", {})
                mode_coverage = counts.setdefault("modeCoverage", {})
                if isinstance(mode_counts, dict) and isinstance(mode_coverage, dict):
                    mode_counts["cloze"] = len(cloze_items)
                    mode_coverage["cloze"] = counts["clozeCoverage"]
                    for mode in DRILL_MODES:
                        mode_items = level_shards.get(mode, {}).get(MODE_BODY_KEYS[mode], [])
                        if not isinstance(mode_items, list):
                            continue
                        mode_counts[mode] = len(mode_items)
                        mode_coverage[mode] = (
                            round(
                                len({item["lemmaId"] for item in mode_items if isinstance(item, dict)})
                                / len(lexemes),
                                4,
                            )
                            if lexemes
                            else 0
                        )


def write_shards(shards: dict[str, dict[str, dict[str, Any]]], out_dir: Path) -> list[Path]:
    out_dir.mkdir(parents=True, exist_ok=True)
    written: list[Path] = []
    for level, level_shards in sorted(shards.items()):
        for kind, payload in sorted(level_shards.items()):
            path = out_dir / f"practice-{kind}.{level}.json"
            path.write_bytes(_json_bytes(payload))
            written.append(path)
    return written


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--atlas-db", type=Path, default=DEFAULT_ATLAS_DB)
    parser.add_argument("--manifest", type=Path)
    parser.add_argument("--out-dir", type=Path, default=DEFAULT_OUT_DIR)
    parser.add_argument("--reviewed-allowlist", type=Path, default=DEFAULT_ALLOWLIST)
    parser.add_argument("--cloze-sources", type=Path, default=DEFAULT_CLOZE_SOURCES)
    parser.add_argument("--heritage-pairs", type=Path, default=DEFAULT_HERITAGE_PAIRS)
    parser.add_argument("--vesum-fixture", type=Path)
    parser.add_argument("--target", type=int, default=DEFAULT_TARGET)
    parser.add_argument("--raw-limit", type=int, default=DEFAULT_RAW_LIMIT)
    parser.add_argument("--gzip-limit", type=int, default=DEFAULT_GZIP_LIMIT)
    parser.add_argument("--fixture-note", type=str)
    parser.add_argument(
        "--broken-validator-fixtures",
        action="store_true",
        help="Run deliberately broken mode-validator fixtures and exit nonzero when validators catch them.",
    )
    args = parser.parse_args(argv)

    if args.broken_validator_fixtures:
        return run_broken_validator_fixtures()

    entries = read_manifest(args.manifest) if args.manifest else read_atlas_db(args.atlas_db)
    allowlist = ReviewedSourceAllowlist.from_path(args.reviewed_allowlist)
    cloze_sources = read_cloze_sources(args.cloze_sources)
    heritage_pairs = read_heritage_pairs(args.heritage_pairs)
    verifier: VesumVerifier = (
        JsonVesumVerifier.from_path(args.vesum_fixture)
        if args.vesum_fixture
        else RealVesumVerifier()
    )
    config = BuildConfig(
        target=args.target,
        raw_limit=args.raw_limit,
        gzip_limit=args.gzip_limit,
        fixture_note=args.fixture_note,
        source_label="fixture" if args.vesum_fixture or args.manifest else "atlas.db",
    )
    shards = build_practice_shards(entries, allowlist, verifier, cloze_sources, config, heritage_pairs)
    apply_size_budgets(shards, config.raw_limit, config.gzip_limit)
    written = write_shards(shards, args.out_dir)
    for path in written:
        print(path)
    return 0


