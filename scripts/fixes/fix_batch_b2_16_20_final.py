def process_file(filepath, module_num):
    print(f"Processing {filepath}...")
    with open(filepath) as f:
        activities = yaml.safe_load(f)

    modified = False

    # 1. Expand Unjumble
    for act in activities:
        if act.get('type') == 'unjumble':
            for item in act.get('items', []):
                ans = item.get('answer', '')
                # Normalize key by stripping punctuation and spaces
                norm_ans = ans.replace('.', '').replace(',', '').replace('?', '').replace('!', '').strip()

                # Check direct match first
                if ans in unjumble_replacements:
                    item['answer'] = unjumble_replacements[ans]
                    item['words'] = [w.strip(".,!?; ") for w in unjumble_replacements[ans].split() if w.strip(".,!?; ")]
                    modified = True
                else:
                    # Check normalized match
                    for key, val in unjumble_replacements.items():
                        norm_key = key.replace('.', '').replace(',', '').replace('?', '').replace('!', '').strip()
                        if norm_ans == norm_key:
                            item['answer'] = val
                            item['words'] = [w.strip(".,!?; ") for w in val.split() if w.strip(".,!?; ")]
                            modified = True
                            break

    # 2. Expand Quiz (Robust)
    for act in activities:
        if act.get('type') in ['quiz', 'select']:
            for item in act.get('items', []):
                q = item.get('question', '')
                if len(q.split()) < 10:
                    matched = False
                    for pattern, repl in quiz_patterns:
                        if re.match(pattern, q):
                            item['question'] = re.sub(pattern, repl, q)
                            modified = True
                            matched = True
                            break
                    # Fallback expansion if no regex match but still short
                    if not matched and "?" in q and not q.startswith("Визначте") and not q.startswith("Укажіть"):
                            new_q = "Визначте, будь ласка: " + q[0].lower() + q[1:]
                            if len(new_q.split()) >= 10:
                                item['question'] = new_q
                                modified = True

    # 3. Add Essay
    if not any(a.get('type') == 'essay-response' for a in activities) and str(module_num) in essay_activities:
        activities.append(essay_activities[str(module_num)])
        print("  Added essay.")
        modified = True

    if modified:
        with open(filepath, 'w') as f:
            yaml.dump(activities, f, allow_unicode=True, sort_keys=False)
        print("  Updated activities.")

# Process Markdown
def fix_markdown(filepath):
    print(f"Checking markdown {filepath}...")
    with open(filepath) as f:
        content = f.read()

    modified = False
    if "## Тест: Прочитайте текст" in content:
        content = content.replace("## Тест: Прочитайте текст", "## Вступ")
        modified = True
    elif "## Вступ" not in content:
        # Insert before first H2
        parts = content.split("\n## ", 1)
        if len(parts) > 1:
            content = parts[0] + "\n\n## Вступ\n\n" + "## " + parts[1]
            modified = True

    # Add Richness boxes if missing (simple check)
    if content.count("> 💡") < 3: # If fewer than 3 boxes
         # Add generic boxes to end of sections
        new_boxes = [
            "\n\n> 💡 **Чи знали ви?**\n>\n> Українська мова є однією з найбагатших слов'янських мов за кількістю синонімів.\n",
            "\n\n> 🌍 **У реальному житті**\n>\n> Використання правильного регістру допомагає будувати успішну кар'єру та стосунки.\n",
            "\n\n> 📚 **Літературний момент**\n>\n> Класики української літератури часто використовували народну мову для створення колориту.\n"
        ]
        # Append to end of Intro, Explanation, Practice
        if "## Вступ" in content and "Чи знали ви?" not in content:
            content = content.replace("## Вступ", "## Вступ" + new_boxes[0], 1)
            modified = True
        if "## Пояснення" in content and "У реальному житті" not in content:
            content = content.replace("## Пояснення", "## Пояснення" + new_boxes[1], 1)
            modified = True

    if modified:
        with open(filepath, 'w') as f:
            f.write(content)
        print("  Updated markdown.")

