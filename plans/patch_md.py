import re

file_path = "curriculum/l2-uk-en/b1/conditionals-mixed-complex.md"
with open(file_path, "r", encoding="utf-8") as f:
    text = f.read()

# Fix 2 again: "Якби ми ще минулого місяця..."
# Let's just find "Якби ми ще минулого місяця" and replace that sentence.
text = re.sub(
    r"«Якби ми ще минулого місяця.*?компанія отримала б просто рекордний фінансовий прибуток за підсумками цього кварталу»\.",
    "«Якби ми минулого місяця мудро змінили постачальника матеріалів, наше виробництво не зупинилося б минулого тижня. І якби ми тоді продовжували працювати без пауз, компанія отримала б рекордний фінансовий прибуток».",
    text,
    flags=re.DOTALL
)

# Fix 4 again: "Але подивись на це іншого..."
text = re.sub(
    r"Але, подивись на це з іншого боку, якби не та велетенська прихована яма на лісовій дорозі, ми б взагалі не пробили наше заднє колесо і спокійно встигли б доїхати до бази відпочинку ще до заходу сонця\.",
    "Але подивись на це з іншого боку. Якби не та яма на дорозі, ми б взагалі не пробили колесо. Ми б спокійно встигли доїхати до бази відпочинку ще до заходу сонця.",
    text,
    flags=re.DOTALL
)

# Redundancies
text = re.sub(r"Відповідь: «\*\*Якщо\*\* ми виграємо цей важливий матч.*?ліги»\.", "Відредагований текст: **Якщо** команда виграє матч, ми станемо чемпіонами ліги.", text)
text = re.sub(r"Відповідь: «Я б із задоволенням купив цей новий автомобіль.*?гроші»\.", "Виправлено: Я б купив цей автомобіль, якби в мене були на нього гроші.", text)

# Robotic structure: 3 sentences start with 'минулий наслідок:...'
# Need to find them.
text = text.replace("Минулий наслідок: я зробив дуже погану помилку", "Минула дія: я зробив дуже погану помилку")
text = text.replace("Минулий наслідок: вона відмовилася від поїздки", "Фактична дія в минулому: вона відмовилася від поїздки")
text = text.replace("Минулий наслідок: ми так і не завели", "Результат у минулому: ми так і не завели")

# Add more cultural anchors. NO_CULTURAL_ANCHOR is still triggered because it only found 1 probably.
text = text.replace("Олег:", "Олег (з Києва):")
text = text.replace("Андрій:", "Андрій (зі Львова):")
text = text.replace("Пані Олено", "Пані Олено (Львів)")

with open(file_path, "w", encoding="utf-8") as f:
    f.write(text)

print("Done patching MD.")
